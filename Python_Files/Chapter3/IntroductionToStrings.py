#Slices or Parts of strings:

Name = "Harry Potter"

Slice = Name[1:11]

print(Slice)

Slice2 = Name[-12:-1]

print(Slice2)

Slice3 = Name[:10]

print(Slice3)

Slice4 = Name[3:]

print(Slice4)

Slice5 = Name[:]

print(Slice5)

Slice6 = Name[:-3]

print(Slice6)

Slice7 = Name[-6:]

print(Slice7)

Character = Name[6]

print(Character)

Character2 = Name[-4]

print(Character2)

# Slicing with skip value:

Char_Out = Name[1:8:3]

print(Char_Out)

Char2_Out = Name[-12:-3:4]

print(Char2_Out)

Slice8 = Name[ : :3]

print(Slice8)

